function prosess() {
    const textarea = document.getElementById("textarea").value.split("\n")
    maintext = textarea
}

function randomNum(min, max) {
    return Math.floor(Math.random() * (max - min)) + min;
}

function generateSong() {
    let firstPartString = document.getElementsByClassName("first-part-text")
    let secondPartString = document.getElementsByClassName("second-part-text")
    let thirdPartString = document.getElementsByClassName("third-part-text")
    let chorusPartString = document.getElementsByClassName("chorus-part")
    let chorusPartString2 = document.getElementsByClassName("chorus-part-2")
    Array.from(firstPartString).forEach((element, i) => {
        let rngN = randomNum(0, maintext.length)
        element.innerText = maintext[rngN]
        if (i == 3) {
            let rngNFS = randomNum(0, endStrings.length)
            element.innerText = endStrings[rngNFS]
        }
    });
    Array.from(secondPartString).forEach((element, i) => {
        let rngN = randomNum(0, maintext.length)
        element.innerText = maintext[rngN]
        if (i == 3) {
            let rngNFS = randomNum(0, endStrings.length)
            element.innerText = endStrings[rngNFS]
        }
    });
    Array.from(thirdPartString).forEach((element, i) => {
        let rngN = randomNum(0, maintext.length)
        element.innerText = maintext[rngN]
        if (i == 3) {
            let rngNFS = randomNum(0, endStrings.length)
            element.innerText = endStrings[rngNFS]
        }
    });
    generateChorus()
    Array.from(chorusPartString).forEach((element, i) => {
        element.innerText = chorusArr[i]
    });
    Array.from(chorusPartString2).forEach((element, i) => {
        element.innerText = chorusArr[i]
    });
}


function generateChorus() {
    chorusArr = []
    for (let i = 0; i < 4; i++) {
        let rngN = randomNum(0, maintext.length)
        chorusArr.push(maintext[rngN])
    }
}

var endStrings = []

function getFinalString() {

    maintext.forEach(str => {
        let splitStr = str.split("")
        for (let i = 0; i < splitStr.length; i++) {
            if (splitStr[i] === ".") {
                let abc = splitStr.join("")
                endStrings.push(abc)
            }
        }

    });
}
